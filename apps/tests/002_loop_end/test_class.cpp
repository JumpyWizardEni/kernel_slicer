//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int32_t array_summ_cpu(const int32_t* array, const size_t arraySize)
{
  Numbers filter;
  filter.CalcArraySumm(array, uint32_t(arraySize));
  return filter.m_summ;
}
